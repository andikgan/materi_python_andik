## Lists
# Lists adalah salah satu tipe variable yang berfungsi untuk menyimpan berbagai macam tipe variabel. List bisa ditambah dan dikurangi isinya.
# lebih detail https://www.w3schools.com/python/python_lists.asp

## contoh
my_list = [1, 'dua', True, [1,2,3]]
print(my_list)

## penjelasan
# my_list adalah nama variable tersebut & variable tersebut bertipe list
# Didalam my_list terdapat 4 variable dengan tipe berbeda-beda

## soal
# Buatlah sebuah list yang berisi 4 variable/isi!
# Setelah membuat list, tambahkan variable baru ke dalam list tersebut (kata kunci=append)
# Ganti list dengan indeks ke 1 dengan variable 'satu' (kata kunci=pelajari index dalam list)