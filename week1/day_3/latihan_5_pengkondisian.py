## Conditions
# Pengkondisian atau conditions adalah pengkondisian kasus dalam programming yang hasilnya boolean (True or False)
# Pengkondisian digunakan dalam pengcabangan logika, misalnya dalam sebauh kasus aplikasi cek gender, ada pengkondisin laki atau wanita
# referensi https://www.learnpython.org/en/Conditions

# contoh
name = 'Candra'
gender = 'laki-laki'

if gender == 'laki-laki':
    print('laki-laki')
else:
    print('perempuan')

#penjelasan
# Script diatas digunakan untuk melakukan pengecekan gender, jika gender = laki-laki maka, akan melakukan print 'laki-laki', jika tidak maka akan print 'perempuan' 

