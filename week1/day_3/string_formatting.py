# String Formatting adalah sebuah operasi untuk memanipulasi variable ke dalam tipe string
# contoh
name = "John"
age = 23
print("%s is %d years old." % (name, age))

#penjelasan
# name adalah variable bertipe string
# age adalah variable bertipe integer
# Kita lakukan print agar menjadi satu kalimat bertipe string "John is 23 years old.
# %s digunakan untuk melakukan formatting tipe string ke dalam string. %d digunakan untuk melakukan formatting tipe integer ke dalam string

