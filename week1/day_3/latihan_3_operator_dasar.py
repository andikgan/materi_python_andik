## Basic Operator
# Basic operator atau operator dasar adalah operasi matematika yang digunakan mengolah variable. Operator disini ada penambahan, pengurangan, perkalian, pembagian dll. 
# Selengkapnya https://www.w3schools.com/python/python_operators.asp

## contoh
number = 1 + 2 * 3 / 4.0
print(number)

