## Basic String Operations
# Basic String Operations atau operasi string dasar adalah teknik melakukan manipulasi terhadap variabel tipe string. Kita bisa mengubah urutan indeks pada string, menghitung jumlah karakter dalam variable string dll. 
# Selengkapnya https://www.learnpython.org/en/Basic_String_Operations

## contoh
astring = "Hello world!"
new_astring = astring[3:7]
print(new_astring)
