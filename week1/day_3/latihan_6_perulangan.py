## Loops
# perulangan atau loops adalah suatu cara dalam dunia programming yang digunakan untuk melakukan perulangan suatu perintah.
# mengapa menggunakan loops? agar sebuah perintah dapat melakukan perulangan berkali-kali tanpa menulis ulang baris code yang sama
# detail https://www.learnpython.org/en/Loops

## contoh
# berikut ini adalah aplikasi untuk melakukan perulangan print sebuah list
list_nama = ['yusuf', 'candra', 'arif']
for nama in list_nama:
    print(nama)

## penjelasan
# kita memiliki sebuah list dengan nama list_nama dengan isi ['yusuf', 'candra', 'arif']
# kita ingin melakukan print semua nama yang ada dalam list tanpa menulis ulang perintah print

## soal
# buatlah aplikasi print nama buah-buahan dalam sebuah list menggunakan syntax 'for'